using UnityEngine;
using UnityEngine.SceneManagement;

public class GameManager : MonoBehaviour
{
    public int playerClass;//플레이어 직업 0:전사 1:마법사 2:궁수
    [SerializeField] GameObject[] playerPrefabs;//플레이어 프리팹 배열 

    //플레이어의 직업을 설정하는 메서드
    public void SetClass(int classNo)
    {
        playerClass = classNo;
    }
    //플레이어 직업을 반환하는 메서드
    public int GetClass()
    {
        return playerClass;
    }

    private void Start()
    {
        Debug.Log("1");
        if (SceneManager.GetActiveScene().name == "MainGame")//MainGame씬에서
        {
            Debug.Log("2");
            //플레이어 프리팹 생성
            Instantiate(playerPrefabs[playerClass], transform.position, Quaternion.identity);
        }
    }

}
